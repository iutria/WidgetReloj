    public class Crometro implements ActionListener{
        public void actionPerformed(ActionEvent evt){
            GregorianCalendar tiempo = new GregorianCalendar();
            String h,m,s,ampm;
            h = tiempo.get(Calendar.HOUR)>9 ? ""+tiempo.get(Calendar.HOUR) : "0"+tiempo.get(Calendar.HOUR);
            h = h.equals("00") ? String.valueOf(12) : h;
            m = tiempo.get(Calendar.MINUTE)>9 ? ""+tiempo.get(Calendar.MINUTE):"0"+tiempo.get(Calendar.MINUTE);
            s = tiempo.get(Calendar.SECOND)>9 ? ""+tiempo.get(Calendar.SECOND) : "0"+tiempo.get(Calendar.SECOND);
            ampm = tiempo.get(Calendar.AM_PM) == Calendar.AM ? "A.M" : "P.M";
            lblHora.setText(h);
            lblMinutos.setText(m);
            lblSegundos.setText(s);
            lblAMmPm.setText(ampm);
        }
    }
